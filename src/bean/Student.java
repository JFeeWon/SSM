package bean;

public class Student {
	private int stuId;
	private int stuName;
	public int getStuId() {
		return stuId;
	}
	public void setStuId(int stuId) {
		this.stuId = stuId;
	}
	public int getStuName() {
		return stuName;
	}
	public void setStuName(int stuName) {
		this.stuName = stuName;
	}
	
}
